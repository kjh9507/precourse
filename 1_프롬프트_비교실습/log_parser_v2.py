def parse_line(log_line: str) -> dict:
    """웹 서버 접속 로그 한 줄을 파싱하여 딕셔너리로 반환합니다.

    :param log_line: 파싱할 로그 문자열 한 줄
    :return: 추출된 정보를 담은 딕셔너리 (파싱 실패 시 None 반환)
    """

# ==========================================
# 테스트 코드
# ==========================================
if __name__ == "__main__":
    # 1. 예시 로그 준비
    sample_log = '127.0.0.1 - - [07/Jul/2026:10:23:45 +0900] "GET /index.html HTTP/1.1" 200 1043'

    # 2. 함수 호출
    result = parse_line(sample_log)

